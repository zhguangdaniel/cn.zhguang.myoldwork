public class IntegerReTry {

	/**
	 * @version 1.0
	 * @param args
	 */
	public static void main(String[] args) {
		int a = 5;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		Long aaa = 567L;
		System.out.println(aaa.toString());
	}

}
